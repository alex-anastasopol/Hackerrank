	/**
	 * @param readTextFile
	 * @return
	 */
	public static String[][] readTextFileToMatrix(String firstRecord,
			String readTextFile) {
		String patru = "input type=\\\"?radio\\\"?.*?value=\\\"?(.*?)\\\".*?>.*?(?:<i>)?(?:.*?)(?:</i>)?(.*?)<a?";
		String pdQueryCase = "<a href=\"/pdquery.php.*?<i>(.*?)</i></a>";
		if (readTextFile.matches(pdQueryCase)) {
			readTextFile = readTextFile.replaceAll(pdQueryCase,
					"$1<a href=''> </a> ");
		}
		List<HashMap<String, String>> options = RegExUtils.getMatchesAsMap(
				patru, readTextFile, 1, 2);

		// StringBuilder l = new StringBuilder("{");
		String[][] a = new String[options.size() + 1][2];
		int i = 1;
		a[0] = new String[] { firstRecord, "Search All Items Below" };
		for (HashMap<String, String> option : options) {
			a[i++] = new String[] {
					StringUtils.cleanHtml(option.get("1")).trim(),
					StringUtils.cleanHtml(option.get("2")).trim() };

			// l = l.append( String.format(" { \"%s\" , \"%s\"  },",
			// option.get("1").trim(), option.get("2").trim()) );
		}

		// l= l.append("}");
		return a;
	}

