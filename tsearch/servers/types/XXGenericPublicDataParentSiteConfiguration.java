	public static List<String[][]> filterOutOptions(String regEXForRuleOut) {
		List<String[][]> filteredListOfOptions = new ArrayList<String[][]>();
		for (String[][] matrix : listOfOptions_state) {
			for (int i = 0; i < matrix.length; i++) {
				String value = matrix[i][0];
				if (value.matches(regEXForRuleOut) || i == 0) {
					filteredListOfOptions.add(new String[][] { { matrix[i][0],
							matrix[i][1] } });
				}
			}
		}
		return filteredListOfOptions;
	}

	public static String[][] filterOutOptions(String[][] matrix,
			String regEXForRuleOut) {
		if (StringUtils.isEmpty(regEXForRuleOut))
			return matrix;

		List<String[][]> filteredListOfOptions = new ArrayList<String[][]>();
		for (int i = 0; i < matrix.length; i++) {
			String value = matrix[i][0];
			if (value.matches(regEXForRuleOut) || i == 0) {
				filteredListOfOptions.add(new String[][] { { matrix[i][0],
						matrix[i][1] } });
			}
		}
		String[][] returnMatrix = new String[filteredListOfOptions.size()][2];
		int i = 0;
		for (String[][] strings : filteredListOfOptions) {
			returnMatrix[i] = strings[0];
			i++;
		}
		return returnMatrix;
	}

